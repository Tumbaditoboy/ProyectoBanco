    private void cargarUsuarios(){
        usuarios = UsuarioDAO.getAll();
        DefaultTableModel modelo =(DefaultTableModel) tblAdmin.getModel();
        modelo.setRowCount(0);
        
        for (Usuario u : usuarios){
            modelo.addRow(new Object[] {
                u.getNombre(),
                u.getCorreo(),
                u.getSaldo()
            });
        }
    }

